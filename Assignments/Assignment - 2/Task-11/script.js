// Task 11: Switch-case program for recharge options
let choice = 2; // You can change this value (1, 2, 3, or any other)

switch (choice) {
    case 1:
        console.log("Data Pack");
        break;
    case 2:
        console.log("Talktime");
        break;
    case 3:
        console.log("Validity Recharge");
        break;
    default:
        console.log("Invalid Choice");
}

